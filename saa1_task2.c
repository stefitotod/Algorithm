// Съставете алгоритъм и напишете програма за намиране на сумата на цифрите на естествено трицифрено число.

#include <stdio.h>

int main() {
  
    int n, a, b, c;

    printf("n = ");
    scanf("%d", &n);
    
    a = n / 100;    
    b = (n / 10) % 10; 
    c = n % 10;     

    printf("S = %d\n", a + b + c);

    return 0;
  
}
